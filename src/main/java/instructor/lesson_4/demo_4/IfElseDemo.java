package instructor.lesson_4.demo_4;

class IfElseDemo {

    public static void main(String[] args) {

        System.out.println("Start"); // 1

        boolean shouldRun = false;

        if (shouldRun) {
            System.out.println("Inside if branch"); // 2
        } else {
            System.out.println("Inside else branch"); // 3
        }

        System.out.println("Finish"); // 4

    }
}
