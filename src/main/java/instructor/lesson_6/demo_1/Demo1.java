package instructor.lesson_6.demo_1;

class Demo1 {

    public static void main(String[] args) {

        System.out.println("Start");

        int i = 0;
        while (i < 1000) {
            System.out.println("INSIDE WHILE LOOP");
            i++;
        }

        System.out.println("Finish");
        System.out.println("i = " + i);

    }
}
